void CPlayer_SSH_Ice::Initialize()
{
    m_tInfo.vPos = { 75.f, 300.f, 0.f };

    D3DXMatrixIdentity(&m_tInfo.matWorld);

    m_vPoint = { 0.f, 0.f, 0.f };
    m_vOriginPoint = m_vPoint;

    m_fSpeed = 1.f;

    m_eRender = RENDER_GAMEOBJECT;
}

